namespace AniSharp.Models
{
    public class Card
    {
        /// <summary>
        /// Gets the name
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Gets the score
        /// </summary>
        public double Score { get; set; }

        /// <summary>
        /// Gets the image url
        /// </summary>
        public string Image { get; set; }

        /// <summary>
        /// Gets the url of the page that contains more data
        /// </summary>
        public string Url { get; set; }
    }
}
